        cầu đến Shopee và sử dụng Shopee Xu để khấu trừ vào giá sản phẩm khi thực hiện giao dịch mua hàng trên Trang Shopee theo quy định của Shopee vào từng thời điểm. &nbsp;Tất cả các khoản hoàn tiền phải tuân thủ theo Chính Sách Trả Hàng và Hoàn
        Tiền tại Mục 14.4.
