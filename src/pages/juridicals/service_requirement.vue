      </p>
      <p class="MsoNormal" style="margin-left:.5in;text-align:justify;text-indent:-.5in;">
        14.2&nbsp;&nbsp;&nbsp;&nbsp; Người Mua có thể yêu cầu trả hàng và hoàn tiền theo&nbsp;<a href="http://www.shopee.vn/docs/3605">Chính Sách Trả Hàng và Hoàn Tiền</a><u>&nbsp;</u>của Shopee.
      </p>
      <p class="MsoNormal" style="margin-left:.5in;text-align:justify;text-indent:-.5in;">
        14.3&nbsp;&nbsp;&nbsp;&nbsp; Shopee không quản lý việc hủy đơn hàng, trả hàng, hoàn tiền của các đơn hàng tự giao dịch giữa Người Bán và Người Mua.
      </p>
      <p class="MsoNormal" style="margin-left:.5in;text-align:justify;text-indent:-.5in;">
        14.4&nbsp;&nbsp;&nbsp;&nbsp; Nếu bạn dùng Shopee Xu để thanh toán và được hoàn trả dựa trên Chính Sách Trả Hàng và Hoàn Tiền của Shopee, Shopee sẽ tách biệt hoàn trả số tiền bạn đã thanh toán cho món hàng đó và số Shopee Xu bạn đã dùng vào Tài Khoản của
        bạn.
