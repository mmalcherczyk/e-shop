      <p class="MsoNormal" style="text-align:justify;">
        <b>15.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
          TRÁCH NHIỆM CỦA NGƯỜI BÁN</b>
      </p>
      <p class="MsoNormal" style="margin-left:.5in;text-align:justify;text-indent:-.5in;">
        15.1&nbsp;&nbsp;&nbsp;&nbsp; Người Bán phải quản lý và đảm bảo độ chính xác và đầy đủ của các thông tin chẳng hạn liên quan đến giá cả và chi tiết sản phẩm, số lượng sản phẩm trong kho cũng như các điều khoản và điều kiện bán hàng được cập nhật trong
        danh mục sản phẩm của Người Bán và không được phép đăng tải các thông tin không chính xác hoặc gây nhầm lẫn.
      </p>
      <p class="MsoNormal" style="margin-left:.5in;text-align:justify;text-indent:-.5in;">
        15.2&nbsp;&nbsp;&nbsp;&nbsp; Giá sản phẩm được Người Bán toàn quyền quyết định, trừ các sản phẩm được ấn định giá bởi cơ quan có thẩm quyền. Giá sản phẩm nên bao gồm toàn bộ số tiền mà Người Mua cần thanh toán (ví dụ: các loại thuế, phí, v.v.) và Người
        Bán sẽ không yêu cầu Người Mua thanh toán thêm hoặc riêng bất kỳ khoản tiền nào khác. Nếu thông tin giá hàng hóa hoặc dịch vụ niêm yết không thể hiện rõ giá đó đã bao gồm hay chưa bao gồm những chi phí liên quan đến việc mua hàng hóa hoặc
        dịch vụ như thuế, phí đóng gói, phí vận chuyển và các chi phí phát sinh khác thì giá này được hiểu là đã bao gồm mọi chi phí liên quan nói trên.
      </p>
      <p class="MsoNormal" style="margin-left:.5in;text-align:justify;text-indent:-.5in;">
        15.3&nbsp;&nbsp;&nbsp;&nbsp; Trong giới hạn pháp luật cho phép, Người Bán đồng ý rằng Shopee có thể, theo toàn quyền quyết định của mình, thực hiện các hoạt động khuyến mãi để hỗ trợ các giao dịch giữa Người Bán và Người Mua thông qua việc giảm, chiết
        khấu hoặc hoàn lại phí hoặc thông qua những cách khác. Giá cuối cùng Người Mua cần thanh toán thực tế là giá đã áp dụng những điều chỉnh trên.
      </p>
      <p class="MsoNormal" style="margin-left:.5in;text-align:justify;text-indent:-.5in;">
        15.4&nbsp;&nbsp;&nbsp;&nbsp; Để thúc đẩy việc mua các sản phẩm Người Bán đăng bán, Shopee có thể đăng những sản phẩm đó (theo mức giá đã điều chỉnh) lên các website của bên thứ ba (chẳng hạn các cổng thông tin và cổng so sánh giá) và những website khác
        (nội địa hoặc quốc tế) được vận hành bởi hoặc hợp tác với Shopee.
      </p>
      <p class="MsoNormal" style="margin-left:.5in;text-align:justify;text-indent:-.5in;">
        15.5&nbsp;&nbsp;&nbsp;&nbsp; Người Bán chịu trách nhiệm cung cấp hóa đơn cho Người Mua theo quy định pháp luật.
      </p>
      <p class="MsoNormal" style="margin-left:.5in;text-align:justify;text-indent:-.5in;">
        15.6&nbsp;&nbsp;&nbsp;&nbsp; Người Bán thừa nhận và đồng ý rằng Người Bán chịu trách nhiệm chi trả các loại thuế, phí hải quan và bất kỳ loại phí nào đối với sản phẩm được bán và Shopee không cung cấp bất kỳ tư vấn về pháp lý hay tư vấn thuế nào liên
        quan đến vấn đề này. &nbsp;Bên Bán nên sử dụng dịch vụ tư vấn thuế chuyên nghiệp nếu cần thiết.
      </p>
      <p class="MsoNormal" style="margin-left:.5in;text-align:justify;text-indent:-.5in;">
        15.7&nbsp;&nbsp;&nbsp;&nbsp; Người Bán thừa nhận và đồng ý rằng bất kỳ sự vi phạm nào của Người Bán đối với các chính sách của Shopee sẽ dẫn đến các biện pháp đề cập tại Mục 7.1.
      </p>
      <p class="MsoNormal" style="text-align:justify;">
            &nbsp;
      </p>
      <p class="MsoNormal" style="text-align:justify;">
        <strong>16.&nbsp; &nbsp; &nbsp; &nbsp;</strong><strong>DỊCH VỤ HIỂN THỊ</strong>
      </p>
      <p class="MsoNormal" style="margin-left:0.5in;text-align:justify;text-indent:-0.5in;">
        16.1&nbsp; &nbsp; &nbsp;Shopee cung cấp dịch vụ quảng cáo tìm kiếm (“Quảng Cáo Tìm Kiếm”) và các dịch vụ quảng cáo khác, quảng cáo hiển thị (“Quảng Cáo Hiển Thị”), quảng cáo khám phá (“Quảng Cáo Khám Phá”) (các dịch vụ này sẽ được gọi chung là “Dịch Vụ
        Hiển Thị”) trên Trang Quảng Cáo tại Kênh Người Bán của Shopee. Người Bán có thể chọn mua Dịch Vụ Hiển Thị. Shopee cung cấp Dịch Vụ Hiển Thị dựa theo Điều Khoản Dịch Vụ này và bất kỳ tài liệu hướng dẫn nào được đăng tải trên Trang Shopee,
        Trang Dịch Vụ Hiển Thị hoặc được thông báo cho Người Bán bằng văn bản (sau đây được gọi là “Điều Khoản Sử Dụng Dịch Vụ Hiển Thị”). Người Bán chọn mua Dịch Vụ Hiển Thị đồng ý tuân thủ và chịu sự điều chỉnh của Điều Khoản Sử Dụng Dịch Vụ Hiển
        Thị. Nếu bạn không đồng ý chịu sự điều chỉnh của Điều Khoản Sử Dụng Dịch Vụ Hiển Thị, vui lòng không chọn sử dụng bất kỳ Dịch Vụ Hiển Thị nào.
      </p>
      <p class="MsoNormal" style="margin-left:0.5in;text-align:justify;text-indent:-0.5in;">
        16.2&nbsp; &nbsp; &nbsp;Để sử dụng Dịch Vụ Hiển Thị, bạn phải là Người Bán đủ điều kiện theo quy định của Điều Khoản Sử Dụng Dịch Vụ Hiển Thị. Vào thời điểm bạn mua và thanh toán cho Dịch Vụ Hiển Thị, Tài Khoản của bạn không được đang ở tình trạng bị
        đình chỉ hoặc tạm ngưng hoạt động.
      </p>
      <p class="MsoNormal" style="margin-left:0.5in;text-align:justify;text-indent:-0.5in;">
        <span>16.3&nbsp; &nbsp; &nbsp;Shopee sẽ lập hóa đơn cho Người Bán dựa trên số lượt hiển thị thực tế nhận được và Người Bán sẽ thanh toán hóa đơn đó theo các điều khoản được nêu trong hóa đơn. Thông tin thanh toán mà Người Bán cung cấp sẽ được sử dụng để tạo hóa đơn và hóa đơn thuế cho Dịch Vụ Hiển Thị. Người Bán chịu trách nhiệm đảm bảo rằng thông tin thanh toán là chính xác trước khi gửi đơn đặt hàng Dịch Vụ Hiển Thị. Bất kỳ sai sót nào trong thông tin thanh toán đã gửi có thể dẫn đến sai thông tin trong hóa đơn và hóa đơn thuế là trách nhiệm của Người Bán. Shopee có quyền từ chối các yêu cầu chỉnh sửa hóa đơn do Người Bán gửi sai thông tin.</span>
      </p>
      <p class="MsoNormal" style="margin-left:0.5in;text-align:justify;text-indent:-0.5in;">
        <span>16.4&nbsp; &nbsp; &nbsp;Người Bán có thể đăng ký sử dụng dịch vụ Quảng Cáo Tìm Kiếm và Quảng Cáo Khám Phá bằng cách thanh toán trước phí dịch vụ quảng cáo trên Trang Dịch Vụ Hiển Thị, và những khoản phí đối với Dịch Vụ Quảng Cáo Tìm Kiếm, Quảng Cáo Khám Phá cũng sẽ được Shopee trừ vào Phí Dịch Vụ Quảng Cáo.&nbsp; Tất cả Phí Dịch Vụ Quảng cáo sẽ chịu thuế giá trị gia tăng, hoặc sau những thời hạn do Shopee đưa ra. Trừ khi được quy định khác đi trong Điều Khoản Sử Dụng Dịch Vụ Hiển Thị, Người Bán sẽ không thể hủy và/hoặc yêu cầu hoàn tiền sau khi đã thanh toán Phí Dịch Vụ Quảng Cáo.</span>
      </p>
      <p class="MsoNormal" style="margin-left:0.5in;text-align:justify;text-indent:-0.5in;">
        <span>16.5&nbsp; &nbsp; &nbsp;Người Bán có thể chọn sử dụng dịch vụ Quảng Cáo Tìm Kiếm, Quảng Cáo Khám Phá và vào thời điểm đăng bán sản phẩm hoặc sau đó. Khi bạn đã đăng ký dịch vụ Quảng Cáo Tìm Kiếm và hoàn tất quy trình thanh toán, Người Bán có thể thiết lập các mức ngân sách, từ khóa, thời gian hiển thị… khác nhau đối với từng sản phẩm theo Điều Khoản Sử Dụng Dịch Vụ Hiển Thị Quảng Cáo Tìm Kiếm dựa trên từ khóa đối với mỗi sản phẩm sẽ được kích hoạt và hết hạn vào các ngày do Người Bán thiết lập từ trước (“Thời Hạn Dịch Vụ Hiển Thị Dựa Trên Từ Khóa”). Các khoản Phí Dịch Vụ Quảng Cáo đã thanh toán sẽ không được hoàn lại.</span>
      </p>
      <p class="MsoNormal" style="text-align:justify;">
        <span>16.6&nbsp; &nbsp; &nbsp;</span><span>Quảng Cáo Hiển Thị</span>
      </p>
      <p class="MsoNormal" style="margin-left:1in;text-align:justify;text-indent:-0.5in;">
        (a)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Phí Dịch Vụ Quảng Cáo Hiển Thị. Phí dịch vụ Quảng Cáo Hiển Thị mà Người Bán phải trả cho Shopee trong một tháng nhất định (“Phí dịch vụ Quảng Cáo Hiển Thị”) sẽ được tính theo ngân sách do Người Bán đặt trên Kênh
        người bán (“Tỷ lệ Quảng Cáo Hiển Thị”).
      </p>
      <p class="MsoNormal" style="margin-left:1in;text-align:justify;text-indent:-0.5in;">
        (b)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Cách Tính Phí Dịch Vụ Quảng Cáo Hiển Thị. Phí Dịch Vụ Quảng Cáo Hiển Thị cho một tháng nhất định sẽ được tính bằng (a) số lần hiển thị trong Ngân Sách Đã Xác Nhận; hoặc (b) Giá Trị Mua Hàng Thực Đã Hoàn Thành (tùy
        theo giá trị nào thấp hơn) nhân với Tỷ Lệ Quảng Cáo Hiển Thị.
      </p>
      <p class="MsoNormal" style="text-align:justify;text-indent:0.5in;">
        (c)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Hóa đơn. Shopee sẽ xuất hóa đơn hàng tháng Phí Dịch Vụ Quảng Cáo Hiển Thị cho Người Bán đã cung cấp thông tin tại&nbsp;<a
          href="https://help.shopee.vn/vn/s/article/T%C3%B4i-mu%E1%BB%91n-l%E1%BA%A5y-h%C3%B3a-%C4%91%C6%A1n-%C4%91%E1%BB%8F-th%C3%AC-ph%E1%BA%A3i-l%C3%A0m-sao-D%C3%A0nh-cho-ng%C6%B0%E1%BB%9Di-b%C3%A1n"
          target="_blank"
        ><span style="color:#4C33E5;">ĐÂY</span></a>
      </p>
      <p class="MsoNormal" style="margin-left:1in;text-align:justify;text-indent:-0.5in;">
        (d)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Thanh Toán Phí Dịch Vụ Quảng Cáo Hiển Thị. Phí Dịch Vụ Quảng Cáo Hiển Thị sẽ được Shopee thanh toán qua (i) tự động khấu trừ từ số dư ví của Người Bán sau khi xác thực hệ thống, hoặc (ii) khấu trừ thủ công từ số
        dư ví của Người Bán trong vòng 3 (ba) ngày do Người Bán thanh toán sau khi nhận được hóa đơn từ Shopee thông qua phương thức thanh toán được chỉ định trong hóa đơn đó. Phí Dịch Vụ Quảng Cáo Hiển Thị do Shopee xác định sẽ được coi là phí cuối
        cùng. Trong trường hợp Shopee không thu được Phí Dịch Vụ Quảng Cáo Hiển Thị do số dư trong ví của Người bán không đủ, Người Bán cần thanh toán Phí Dịch Vụ Quảng Cáo Hiển Thị chưa thanh toán qua phương thức chuyển khoản trực tiếp vào tài khoản
        ngân hàng của Shopee được chỉ định trong vòng 2 (hai) ngày kể từ ngày Người Bán nhận được thông báo từ Shopee. Người Bán sẽ chịu mọi khoản phí phát sinh để thực hiện thanh toán.
      </p>
      <p class="MsoNormal" style="margin-left:1in;text-align:justify;text-indent:-0.5in;">
        (e)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Không đủ tiền. Trong trường hợp Shopee không thể khấu trừ Phí Dịch Vụ Quảng Cáo Hiển Thị từ số dư ví của Người Bán do không đủ tiền và Người Bán không thanh toán trong thời hạn nhất định nêu tại Điều 16.6. (d) Shopee
        có thể tạm dừng ví của Người Bán và từ chối / lên danh sách đen tất cả dịch vụ của Shopee cho đến khi Người Bán thanh toán Phí Dịch Vụ Quảng Cáo Hiển Thị quá hạn cho Shopee. Trong trường hợp Người Bán Thanh Toán Phí Dịch Vụ Quảng Cáo Hiển
        Thị quá hạn trước 16:00, Shopee sẽ bỏ khóa ví của Người bán và đưa vào danh sách trắng tất cả dịch vụ của Shopee trong cùng ngày làm việc; các trường hợp còn lại sẽ được thực hiện vào ngày làm việc tiếp theo.
      </p>
