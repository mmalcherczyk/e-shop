      </p>
      <p class="MsoNormal" style="margin-left:.5in;text-align:justify;text-indent:-.5in;">
        13.3&nbsp;&nbsp;&nbsp;&nbsp; Người Sử Dụng hiểu rằng Người Bán chịu toàn bộ rủi ro liên quan đến việc vận chuyển hàng hóa được mua và bảo đảm rằng Người Bán đã hoặc sẽ mua bảo hiểm hàng hóa, bao gồm cả việc vận chuyển. Trong trường hợp Hàng hóa được mua
        bị hư hỏng, thất lạc hoặc không chuyển phát được trong quá trình vận chuyển, Người Sử Dụng thừa nhận và đồng ý rằng Shopee sẽ không chịu trách nhiệm đối với bất kỳ tổn thất, chi phí, phí tổn hoặc bất kỳ khoản phí nào phát sinh từ sự cố đó
        và Người Bán và/hoặc Người Mua sẽ liên hệ với đơn vị vận chuyển để giải quyết sự cố đó.
      </p>
      <p class="MsoNormal" style="margin-left:.5in;text-align:justify;text-indent:-.5in;">
        13.4&nbsp;&nbsp;&nbsp;&nbsp; Đối với Giao Dịch Xuyên Quốc Gia.&nbsp;
      </p>
      <p class="MsoNormal" style="margin-left:.5in;text-align:justify;text-indent:-.5in;">
        &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;Người Sử Dụng hiểu rằng, khi một đăng bán có mô tả rằng sản phẩm được đăng bán sẽ được gửi từ nước ngoài ("Sản Phẩm Ở Nước Ngoài"), sản phẩm đó được bán bởi Người Bán ngoài Việt Nam, và việc xuất/nhập khẩu sản
        phẩm đó chịu sự điều chỉnh của pháp luật.&nbsp;Người Sử Dụng cần hiểu rõ các hạn chế về xuất/nhập khẩu hàng hóa của quốc gia nhập khẩu.&nbsp;Người Sử Dụng đồng ý rằng Shopee không thể tư vấn pháp lý liên quan đến vấn đề này và đồng ý rằng
        Shopee sẽ không thể chịu các rủi ro hoặc trách nhiệm pháp lý đối với hoạt động xuất/nhập khẩu hàng hóa vào Việt Nam.
      </p>
      <p class="MsoNormal" style="margin-left:.5in;text-align:justify;text-indent:-.5in;">
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; &nbsp; Bằng việc chọn mua Sản Phẩm Ở Nước&nbsp;Ngoài, Người Sử Dụng theo đây đồng ý ủy thác cho Shopee, tự mình hoặc thông qua bên thứ ba, thực hiện các thủ tục/công đoạn cần thiết cho việc nhập khẩu
        Sản Phẩm Ở Nước Ngoài vào Việt Nam theo quy định pháp luật.
      </p>
